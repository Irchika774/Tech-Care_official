import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
    "inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium ring-offset-background transition-all duration-200 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
    {
        variants: {
            variant: {
                default: "bg-white text-black hover:bg-gray-100 shadow-sm",
                destructive:
                    "bg-red-600 text-white hover:bg-red-700 shadow-sm",
                outline:
                    "border border-zinc-700 bg-transparent text-white hover:bg-zinc-800 hover:text-white",
                secondary:
                    "bg-zinc-800 text-zinc-100 hover:bg-zinc-700 border border-zinc-700",
                ghost: "text-zinc-300 hover:bg-zinc-800 hover:text-white",
                link: "text-blue-400 underline-offset-4 hover:underline",
                premium: "bg-gradient-to-r from-green-600 to-blue-600 text-white hover:from-green-700 hover:to-blue-700 shadow-lg shadow-green-500/20",
                success: "bg-green-600 text-white hover:bg-green-700 shadow-sm",
            },
            size: {
                default: "h-10 px-4 py-2",
                sm: "h-9 rounded-md px-3 text-xs",
                lg: "h-12 rounded-md px-8 text-base",
                icon: "h-10 w-10",
            },
        },
        defaultVariants: {
            variant: "default",
            size: "default",
        },
    }
)

const Button = React.forwardRef(({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
        (<Comp
            className={cn(buttonVariants({ variant, size, className }))}
            ref={ref}
            {...props} />)
    );
})
Button.displayName = "Button"

export { Button, buttonVariants }
